using System;
using Microsoft.SharePoint;

namespace Universe.Sp.Common.EventReceiver
{
    /// <summary>
    ///     Установка EventFiringEnabled = false;
    ///     Set EventFiringEnabled = false;
    /// <author>Alex Envision</author>
    /// </summary>
    /// <seealso cref="Microsoft.SharePoint.SPEventReceiverBase"/>
    /// <seealso cref="System.IDisposable"/>
    public sealed class SpDisabledEventFiringScope : SPEventReceiverBase, IDisposable
    {
        private readonly bool _originalValue;

        /// <summary>
        ///     Initializes a new instance of the <see cref="SpDisabledEventFiringScope"/> class.
        /// </summary>
        public SpDisabledEventFiringScope()
        {
            _originalValue = EventFiringEnabled;
            EventFiringEnabled = false;
        }

        /// <summary>
        ///     Восстановление исходного значения для EventFiringEnabled.
        ///     Restore original value to EventFiringEnabled.
        /// </summary>
        public void Dispose()
        {
            EventFiringEnabled = _originalValue;
        }
    }
}