namespace Universe.Sp.CQRS.Models.Condition
{
    /// <summary>
    /// Модель конфигурации поля
    /// <author>Alex Envision</author>
    /// </summary>
    public class FieldConfiguration : IFieldConfiguration
    {
        /// <summary>
        /// Ключ поля
        /// </summary>
        public string SpFieldName { get; set; }

        /// <summary>
        /// Лейбл
        /// </summary>
        public string Label { get; set; }

        /// <summary>
        /// Тип поля
        /// </summary>
        public virtual string Type { get; set; }

        /// <summary>
        /// Условия выборки
        /// </summary>
        public ConditionConfiguration Condition { get; set; }

        /// <summary>
        /// Форматирование
        /// </summary>
        public string Format { get; set; }
    }
}
