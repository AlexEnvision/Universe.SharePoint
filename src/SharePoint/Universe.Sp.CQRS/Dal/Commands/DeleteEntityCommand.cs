using System;
using System.Threading.Tasks;
using Universe.Sp.CQRS.Dal.Commands.Base;
using Universe.Sp.CQRS.Dal.Commands.CommandResults;
using Universe.Sp.CQRS.Extensions;
using Universe.Sp.DataAccess.Models;

namespace Universe.Sp.CQRS.Dal.Commands
{
    /// <summary>
    ///     Команда удаления сущности.
    /// <author>Alex Envision</author>
    /// </summary>
    /// <typeparam name="TEntitySp"></typeparam>
    public class DeleteEntityCommand<TEntitySp> : BaseCommand
        where TEntitySp : EntitySp, new()
    {
        public virtual DeleteEntityResult Execute(TEntitySp entitySp)
        {
            if (entitySp == null)
                throw new ArgumentNullException(nameof(entitySp));

            var setSp = SpCtx.Set<TEntitySp>();
            var deletedEntity = setSp.Remove(entitySp);

            return new DeleteEntityResult
            {
                Id = deletedEntity.Id,
                IsSuccessful = true
            };
        }
    }
}