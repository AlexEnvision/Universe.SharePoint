using System.Collections.Generic;
using System.Linq;
using AutoMapper;
using Universe.Sp.CSOM.CQRS.Dal.MetaInfo;
using Universe.Sp.CSOM.CQRS.Models;
using Universe.Sp.CSOM.CQRS.Models.Filter;
using Universe.Sp.CSOM.CQRS.Models.Sort;

namespace Universe.Sp.CSOM.CQRS.Extensions
{
    public static class SortingExtensions
    {
        public static QueryBuilder<T> ApplySortingAtQuery<T>(this QueryBuilder<T> query,
            List<SortConfiguration> sorting, QueryableMetaInfo<T> mi)
            where T : class
        {
            var sortDescriptors = sorting?.Select(Mapper.Map<SortConfiguration, CamlSortRule>).ToList();

            QueryBuilder<T> possiballyFilteredQuery = query;
            possiballyFilteredQuery = possiballyFilteredQuery.OrderBy(sortDescriptors);

            if (possiballyFilteredQuery != null)
                query = possiballyFilteredQuery;
            return query;
        }
    }
}
